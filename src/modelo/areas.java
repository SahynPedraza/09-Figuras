public interface areas {
	/**
	 * 
	 * @param n1 variable para calcular area ingresando un numero
	 * @return
	 */
	public double Acuadrado(double n1);
	/**
	 * 
	 * @param n1 variable para calcular area ingresando un numero
	 * @param n2 variable para calcular area ingresando un segundo numero
	 * @return
	 */
	public double Arectangulo(double n1, double n2);
	/**
	 * 
	 * @param n1 variable para calcular area ingresando un numero
	 * @param n2 variable para calcular area ingresando un segundo numero
	 * @return
	 */
	public double Atriangulo(double n1, double n2);
	/**
	 * 
	 * @param n1 variable para calcular area ingresando un numero
	 * @return
	 */
	public double Acirculo(double n1);
	

}
